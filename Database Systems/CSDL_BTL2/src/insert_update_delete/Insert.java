    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;"
            + "databaseName=CSDL_BTL2;"
            + "integratedSecurity=true";
            Connection con = DriverManager.getConnection(url);
            String query = "insert into Groups(GroupName, GroupDescription) values (?, ?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, groupName.getText());
            pst.setString(2, groupDescription.getText());
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)jTable_Display_Group.getModel();
            model.setRowCount(0);
            show_group();
            JOptionPane.showMessageDialog(null, "Inserted Sucessfully!");
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_insertActionPerformed

