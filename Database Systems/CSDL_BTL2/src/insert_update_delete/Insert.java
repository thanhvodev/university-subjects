    public ArrayList<Group> groupList() {
        ArrayList<Group> groupvList = new ArrayList<>();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;"
            + "databaseName=CSDL_BTL2;"
            + "integratedSecurity=true";
            Connection con = DriverManager.getConnection(url);
            String query1 = "SELECT * FROM Groups";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            
            Group group;
            
            while(rs.next()) {
                group = new Group(rs.getString("GroupID"),rs.getString("GroupName"), rs.getString("GroupDescription"), rs.getString("CreatDate"));
                groupvList.add(group);
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        return groupvList;
        
    }
    
    public void show_group() {
        ArrayList<Group> list = groupList();
        DefaultTableModel model = (DefaultTableModel)jTable_Display_Group.getModel();
        Object[] row = new Object[4];
        
        for(int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getGroupID();
            row[1] = list.get(i).getGroupName();
            row[2] = list.get(i).getGroupDescription();
            row[3] = list.get(i).getCreatDate();
            model.addRow(row);
        }
    }

